  void _computeRoute() {
    // Define the route between the two markers
    const List<LatLng> routeCoordinates = [
      LatLng(37.42796133580664, -122.085749655962), // Google Plex
      LatLng(37.43296265331129, -122.08832357078792), // Lake
    ];

    setState(() {
      _polylines.add(
        Polyline(
          polylineId: const PolylineId('route'),
          points: routeCoordinates,
          color: Colors.blue,
          width: 5,
        ),
      );
    });
  }

  Future<void> _goToTheLake() async {
    final GoogleMapController controller = await _controller.future;
    await controller.animateCamera(CameraUpdate.newCameraPosition(_kLake));
  }
